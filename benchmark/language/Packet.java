public class Packet {
		
		private int a,b,c,d,e,result;
		
		public Packet() {
			a = 0;
			b = 0;
			c = 0;
			d = 0;
			e = 0;
			result = -1;
		}
		
		public int getResult() {
			return result;
		}
		
		public void setResult(int result) {
			this.result = result;
		}

		public int getA() {
			return a;
		}

		public void setA(int a) {
			this.a = a;
		}

		public int getB() {
			return b;
		}

		public void setB(int b) {
			this.b = b;
		}

		public int getC() {
			return c;
		}

		public void setC(int c) {
			this.c = c;
		}

		public int getD() {
			return d;
		}

		public void setD(int d) {
			this.d = d;
		}

		public int getE() {
			return e;
		}

		public void setE(int e) {
			this.e = e;
		}
		
	}